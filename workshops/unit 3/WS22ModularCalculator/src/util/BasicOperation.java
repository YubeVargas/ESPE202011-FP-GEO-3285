public class BasicOperation {

    public static float multiplicateNumbers (float number1, float number2) {
        float multiplication = 0;
        multiplication = number1 * number2;
        return multiplication;
    }
    public static float divideNumbers (float number1, float number2) {
        float divition = 0;
        divition = number1 / number2;
        return divition;
    }
}
    

